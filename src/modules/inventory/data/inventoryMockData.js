export const getDaysUntil = (expiryDate, referenceDate = INVENTORY_REFERENCE_DATE) => {
  const expiry = new Date(expiryDate);
  const reference = new Date(referenceDate);
  if (Number.isNaN(expiry.getTime()) || Number.isNaN(reference.getTime())) return null;
  return Math.ceil((expiry.getTime() - reference.getTime()) / (1000 * 60 * 60 * 24));
};

export const getMedicineStatus = (medicine, referenceDate = INVENTORY_REFERENCE_DATE) => {
  const daysLeft = getDaysUntil(medicine.expiryDate, referenceDate);

  if (daysLeft !== null && daysLeft < 0) return "Expired";
  if ((medicine.quantity || 0) <= 0) return "Out of Stock";
  if (daysLeft !== null && daysLeft <= 60) return "Expiring Soon";
  if ((medicine.quantity || 0) <= (medicine.minimumStock || 0)) return "Low Stock";
  return "In Stock";
};

export const createMedicineRecord = (medicine) => ({
  ...medicine,
  status: getMedicineStatus(medicine),
  stockValue: (Number(medicine.quantity) || 0) * (Number(medicine.sellingPrice) || 0),
  daysLeft: getDaysUntil(medicine.expiryDate),
});

